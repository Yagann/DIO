public class Calculadora {
       public static void soma(double numeroUm, double numeroDois){

       double resultado = numeroUm + numeroDois;

       System.out.println("A soma de " + numeroUm + " + " + numeroDois + " é: " + resultado);
       }

       public static void subtracao(double numeroUm, double numeroDois){

        double resultado = numeroUm - numeroDois;
 
        System.out.println("A subtração de " + numeroUm + " - " + numeroDois + " é: " + resultado);
        }

        public static void multiplicacao(double numeroUm, double numeroDois){

            double resultado = numeroUm * numeroDois;
     
            System.out.println("A multiplicação de " + numeroUm + " * " + numeroDois + " é: " + resultado);
            }

            public static void divisao(double numeroUm, double numeroDois){

                double resultado = numeroUm / numeroDois;
         
                System.out.println("A divisão de " + numeroUm + " / " + numeroDois + " é: " + resultado);
                }

    }

